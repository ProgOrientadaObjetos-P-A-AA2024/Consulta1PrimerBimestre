# Crear una lista
frutas = ['manzana', 'plátano', 'cereza']

# Agregar un elemento
frutas.append('naranja')

# Insertar un elemento en una posición específica
frutas.insert(1, 'mango')

# Eliminar un elemento
frutas.remove('cereza')

# Ordenar la lista
frutas.sort()

# Imprimir la lista final
print(frutas)
